def refine_translation(term: str, zh: str) -> str:
    replacements = [
        ('  ', ' '), ('圖 圖', '圖'), ('風 風', '風'), ('流 流', '流'), ('層 層', '層'),
        ('等壓 的', '等壓的'), ('等熵 的', '等熵的'), ('等溫 的', '等溫的'), ('各向同性 的', '各向同性的'),
        ('儀器 飛航 規則', '儀器飛航規則'), ('紅外 影像', '紅外影像'), ('紅外 輻射', '紅外輻射'),
        ('紅外 光譜儀', '紅外光譜儀'), ('內部 邊界層', '內部邊界層'), ('熱帶輻合 帶', '熱帶輻合帶'),
        ('等風速 圖', '等風速圖'), ('等雨量 圖', '等雨量圖'), ('等壓 面圖', '等壓面圖'),
    ]
    for a,b in replacements:
        zh = zh.replace(a,b)
    zh = re.sub(r'([\u4e00-\u9fff])\s+([\u4e00-\u9fff])', r'\1\2', zh)
    return zh.strip()


