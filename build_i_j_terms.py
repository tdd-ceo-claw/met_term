def polish(text: str) -> str:
    text = re.sub(r'\s+', ' ', text).strip()
    text = text.replace(' ,', '，').replace(' .', '。')
    if not text.endswith('。'):
        text += '。'
    text = text.replace(' 0°C', '0°C').replace(' 90 至 150', '90 至 150')
    return text


def generic_translation(term: str) -> str:
    if term in exact_translation:
        return exact_translation[term]
    t = term
    for en in sorted(phrase_map, key=len, reverse=True):
        t = re.sub(rf'(?i)\b{re.escape(en)}\b', phrase_map[en], t)
    t = re.sub(r'\b(of|the|and|on)\b', '', t, flags=re.I)
    t = t.replace('–', '—')
    t = t.replace('-', '—') if re.search(r'[A-Za-z]+-[A-Za-z]+', term) and term.lower() not in ['jet-effect wind','ice-accretion indicator','ice-bulb temperature','ice-crystal cloud','ice-crystal haze'] else t
    t = re.sub(r'\s+', ' ', t).strip(' -—')
    if re.fullmatch(r'[A-Z][A-Z0-9\-]*', term):
        return term
    return t


